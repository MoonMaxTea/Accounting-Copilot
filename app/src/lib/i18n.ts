export type MessageKey = keyof (typeof messages)["en"];

export function t(locale: Locale, key: MessageKey): string {
  return messages[locale][key];
}

export function tf(
  locale: Locale,
  key: MessageKey,
  vars: Record<string, string | number>,
): string {
  return t(locale, key).replace(/\{(\w+)\}/g, (_, name: string) =>
    String(vars[name] ?? `{${name}}`),
  );
}

export function navLabel(locale: Locale, id: string): string {
  switch (id) {
    case "accounting-standards":
      return t(locale, "accountingStandards");
    case "listing-rules":
      return t(locale, "listingRules");
    case "tax":
      return locale === "zh" ? "税法" : "Tax Law";
    case "IFRS":
      return "IFRS";
    case "IAS":
      return "IAS";
    case "ASC":
      return "ASC";
    case "HK":
      return locale === "zh" ? "香港" : "HK";
    case "SEC":
      return locale === "zh" ? "美国SEC" : "SEC";
    case "CN":
      return locale === "zh" ? "中国大陆" : "CN";
    case "DE":
      return locale === "zh" ? "德国" : "DE";
    case "INTL":
      return locale === "zh" ? "国际" : "International";
    case "US":
      return locale === "zh" ? "美国" : "US";
    case "ALL":
      return t(locale, "all");
    default:
      return id;
  }
}
